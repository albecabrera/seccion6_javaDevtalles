package bucles;

public class Bucle {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println("Valor: = " + i);

        }
        System.out.println("Sale del bucle");
        for (int j = 0; j < 9; j+=2) {
            System.out.println("Segundo valor: = " + j);
            System.out.println("Segundo valor: = " + j);

        }
    }

}
