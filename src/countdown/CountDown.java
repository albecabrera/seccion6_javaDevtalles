package countdown;

public class CountDown {
    public static void main(String[] args) {
        // Abzählen, countdown
        int a = 10;
        int b = 0;

        while (a>=b){
            System.out.println("Countdown = " + a);
            a--;
        }
        System.out.println("Nun beginnt die Party!!!🎉");
    }
}
